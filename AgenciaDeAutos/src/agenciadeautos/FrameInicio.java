    private void eliminarItemSeleccionado() {
    int indiceSeleccionado = listaPrincipal.getSelectedIndex();
    if (indiceSeleccionado != -1) {  // Verifica que se haya seleccionado un elemento
        DefaultListModel<String> modeloLista = (DefaultListModel<String>) listaPrincipal.getModel();
        modeloLista.removeElementAt(indiceSeleccionado);
    }
}
    
