    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
          
        String criterio = txtBuscar.getText();
        DefaultListModel<String> modeloResultado = new DefaultListModel<>();

        for (int i = 0; i < listaModelo.size(); i++) {
            String elemento = listaModelo.getElementAt(i).toString();
            if (elemento.contains(criterio)) {
                modeloResultado.addElement(elemento);
            }
        }

        listaPrincipal.setModel(modeloResultado);
     
     
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void comboCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboCategoriaActionPerformed
        
        
                String categoriaSeleccionada = (String) comboCategoria.getSelectedItem();
        DefaultListModel<String> modeloFiltrado = new DefaultListModel<>();

        if (categoriaSeleccionada.equals("Todos")) {
     
            listaPrincipal.setModel(listaModelo);
        } else {
  
            for (int i = 0; i < listaModelo.size(); i++) {
                String elemento = listaModelo.getElementAt(i).toString();
  
                if (elemento.startsWith(categoriaSeleccionada)) {
                    modeloFiltrado.addElement(elemento);
                }
            }

            listaPrincipal.setModel(modeloFiltrado);
        }
        
        //.........................................................
        
  
        
        
        
        
        
    }//GEN-LAST:event_comboCategoriaActionPerformed

